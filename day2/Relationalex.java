package day2;

public class Relationalex {

	public static void main(String[] args) {

// 2) Relational/ comparison operators > >= < <= != ==
		
		int a = 10;
		int b = 20;
		
		System.out.println(a>b);// false
		System.out.println(a>=b);// false
		System.out.println(a<b);// true
		System.out.println(a<=b);// true
		System.out.println(a!=b);// true
		System.out.println(a==b);//false
		
		
		
		
		
		

	}

}
