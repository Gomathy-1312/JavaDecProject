package day2;

public class Logicalex {

	public static void main(String[] args) {
		//3) Logical operators && || !
		
		
		boolean a = true;
		boolean b = false;
		
		System.out.println(a&&b);// false
		System.out.println(a||b);// true
		System.out.println(!a);//false
		System.out.println(!b);//true
		
	}

}
